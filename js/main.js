/* ============================================================
   Active nav link — Intersection Observer
   ============================================================ */
const sections  = document.querySelectorAll('section[id]');
const navLinks  = document.querySelectorAll('.nav-link');

const sectionObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return;
    const id = entry.target.id;
    navLinks.forEach(link =>
      link.classList.toggle('active', link.getAttribute('href') === `#${id}`)
    );
  });
}, { rootMargin: '-35% 0px -65% 0px' });

sections.forEach(s => sectionObserver.observe(s));

/* ============================================================
   Scroll Reveal — Intersection Observer
   ============================================================ */
const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('revealed');
    }
  });
}, { rootMargin: '0px 0px -60px 0px', threshold: 0.08 });

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

/* ============================================================
   Mobile Menu
   ============================================================ */
const hamburger = document.getElementById('hamburger');
const navMenu   = document.getElementById('navMenu');

hamburger.addEventListener('click', () => {
  const isOpen = navMenu.classList.toggle('open');
  hamburger.classList.toggle('open', isOpen);
  document.body.style.overflow = isOpen ? 'hidden' : '';
});

navMenu.querySelectorAll('.nav-link').forEach(link => {
  link.addEventListener('click', () => {
    navMenu.classList.remove('open');
    hamburger.classList.remove('open');
    document.body.style.overflow = '';
  });
});

/* ============================================================
   Contact Form — POSTs to Node.js backend
   TODO: change CONTACT_API to your deployed backend URL
   ============================================================ */
const CONTACT_API = 'http://localhost:3000/api/contact';

const contactForm = document.getElementById('contactForm');
const submitBtn   = document.getElementById('submitBtn');

contactForm.addEventListener('submit', async e => {
  e.preventDefault();

  const name    = contactForm.querySelector('#name').value.trim();
  const email   = contactForm.querySelector('#email').value.trim();
  const message = contactForm.querySelector('#message').value.trim();
  if (!name || !email || !message) return;

  const t = translations[currentLang];
  const origText = submitBtn.textContent;
  submitBtn.textContent = '...';
  submitBtn.disabled = true;

  try {
    const res = await fetch(CONTACT_API, {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify({ name, email, message }),
    });

    if (!res.ok) throw new Error('server');

    submitBtn.textContent = t['contact.form.sent'];
    submitBtn.classList.add('success');
    setTimeout(() => {
      submitBtn.textContent = t['contact.form.send'];
      submitBtn.classList.remove('success');
      submitBtn.disabled = false;
      contactForm.reset();
    }, 3500);

  } catch {
    submitBtn.textContent = '✗ Failed — try emailing directly';
    submitBtn.style.cssText = 'background:#ef4444;box-shadow:none';
    setTimeout(() => {
      submitBtn.textContent = origText;
      submitBtn.style.cssText = '';
      submitBtn.disabled = false;
    }, 4000);
  }
});

/* ============================================================
   Profile photo — show initials when photo.jpg is missing
   ============================================================ */
const profilePhoto   = document.querySelector('.profile-photo');
const photoInitials  = document.querySelector('.photo-initials');

if (profilePhoto && photoInitials) {
  const showPhoto = () => {
    profilePhoto.style.display = 'block';
    photoInitials.style.display = 'none';
  };
  const showInitials = () => {
    profilePhoto.style.display = 'none';
    photoInitials.style.display = 'flex';
  };

  if (profilePhoto.complete) {
    profilePhoto.naturalWidth > 0 ? showPhoto() : showInitials();
  } else {
    profilePhoto.addEventListener('load',  showPhoto);
    profilePhoto.addEventListener('error', showInitials);
  }
}

/* ============================================================
   Scroll Progress Bar
   ============================================================ */
const progressBar = document.getElementById('scrollProgress');
if (progressBar) {
  window.addEventListener('scroll', () => {
    const scrolled = window.scrollY;
    const total    = document.documentElement.scrollHeight - window.innerHeight;
    progressBar.style.transform = `scaleX(${total > 0 ? scrolled / total : 0})`;
  }, { passive: true });
}

/* ============================================================
   Hero Name — letter-by-letter animation
   ============================================================ */
function splitHeroName() {
  const h1 = document.querySelector('.hero-name');
  if (!h1) return;

  const reduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  if (reduced) return; /* CSS fallback already shows text */

  const words = h1.textContent.trim().split(/\s+/);
  h1.textContent = '';

  words.forEach((word, wi) => {
    const wordSpan = document.createElement('span');
    wordSpan.className = 'name-word';

    [...word].forEach((char, ci) => {
      const s = document.createElement('span');
      s.className = 'name-char';
      s.textContent = char;
      s.style.animationDelay = `${(wi * word.length + ci) * 0.042 + 0.12}s`;
      wordSpan.appendChild(s);
    });

    h1.appendChild(wordSpan);
  });
}
splitHeroName();

